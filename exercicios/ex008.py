metros = int(input("Valor em metros:"))
centimetros = metros * 100
milimetros = metros * 1000
kilometro = metros / 1000
hectometro = metros / 100
decametro = metros / 10
decimetro = metros * 10
print ("O valor {} em metros convertidos para centimetros é {} e milimetros é {}, kilometros {}, hectometro {}, decametro {}, decimetro {}.".format(metros,centimetros, milimetros,kilometro,hectometro,decametro,decimetro))